#주석 메모를 할 때 사용함

# , 로 두개를 연결할 수 있다. 숫자도 가능
print("안녕하세요. 반갑습니다")
print("안녕하세요.", "반갑습니다")
print(50, "살 입니다.")
print('안녕','hi','hello') # , 사용시 한칸 띄워진다

# +를 사용해서 연결할 수 있다. 띄어쓰기가 없다.( , 와 다른점 )
print("안녕하세요"+"반갑습니다")
print("50"+"살 입니다.")
print(str(50)+"살입니다")