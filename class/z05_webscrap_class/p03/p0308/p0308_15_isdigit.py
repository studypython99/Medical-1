#   지금의 문자열이(ㅇㅇㅇ)로 가능한가!?

print("1234".isdigit()) #   문자열이 숫자로 치환 가능한가   #   True
print("abc".isalpha())  #   영문으로 치환 가능한가  #   True
print("ab1c11".isalpha())  #   영문으로 치환 가능한가  #   False
print("abaaaa".islower())  #   소문자로 치환 가능한가  #   true
print("abaaaa".isupper())  #   대문자로 치환 가능한가  #   False
print("   ".isspace())  #   빈공백이 있는지 확인 > 문자열이 공백인지
