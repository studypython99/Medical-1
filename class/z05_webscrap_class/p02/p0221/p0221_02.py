#   3. 정수, 실수, 문자열로 출력
#   정수: %d 실수: %f 문자열: %s
print("%d"%150.15) # 정수 소수점이하 없다
print("%f"%150.15) # 실수 %150.15 전체 표시
print("%s"%"150.15") # 문자열

#   4. * 열개 출력
